                <h1 class="tera">Social:Mortgage FAQ</h1>
                <p class="alpha"></p>
            </header>

            <div class="row">
                <div class="cf margin-btm-large">
                    <div class="medium-12 large-12">
                        <h3>What is a social: mortgage?</h3>
<p>Currently the only product of its kind in the UK, it is called a social mortgage for 3 reasons.</p>
<ol>
<li>for every eligible mortgage customer, ASB will donate a cashback sum to an organisation they either work for; volunteer with or sit on it’s governing board.</li>
<li>the customer is supporting an independent mutual ethical bank that is focuses on helping local communities and</li>
<li> it is ensuring that the third sector and its personnel are getting access to financial products and services that are  aligned to their values.</li>
</ol>
<h3>How do I apply for a social:mortgage with ASB?</h3>
<p>It the same process as any of our mortgages… Click or call</p>
<p>Or easiest way is to click on the website and go to the mortgage <a href="../personal/mortgages-and-bridging-loans/index.php" title="Mortgages and bridging loans">page,</a> that’s your best starting point, complete the form and we’ll contact you. </p>
<p>Or, if you prefer, call us on  to make an appointment for an advisor to call you back.</p>
<h3>Do all your mortgage products have this social mortgage offer?</h3>
<p>Yes, all residential mortgages are included, provided you meet the third sector criteria element</p>
<h3>How does the third sector criteria element work to get the £250 cashback</h3>
<p>Your organisation must have some connection to a Scottish Community Re-investment Trust (SCRT) member organisation. The SCRT <a href="https://www.scrt.scot/help-scrt-grow/current-members/" target="_blank" title="SCRT website">website</a> lists all their members.<a href="https://www.scrt.scot/help-scrt-grow/current-members/"><br /></a></p>
<p>If your organisation is a member of one of the listed members (for example Scottish Community Alliance is a member, but Glasgow West of Scotland Forum is not listed on the SCRT website. However, they (GWSF) is a member of the Scottish Community Alliance). So, regardless of how many degrees of separation, if you can demonstrate a connection to SCRT, then your organisation qualifies.</p>
<p>Individuals must be either currently</p>
<ul class='lister margin-btm-xlarge'>
<li>employed by the organisation (full time or part time) or</li>
<li>a volunteer with the organisation or</li>
<li>on the governing board.</li>
</ul>
<h3>How is the £250 claimed and paid?</h3>
<p>To prove the link, we will need confirmation on headed paper signed by the senior employee saying that you are connected to the organisation. (If you are the senior employee then the Chair of the organisation should sign the paper).</p>
<p>Then, when your mortgage is approved and drawn down we will send a cheque for £250 to the address listed on the headed paper.</p>
<h3>What is SCRT and why does my organisation have to be linked to it to meet the criteria?</h3>
<p>The SCRT is a membership organisation that ASB is working with to promote financial products and services to third sector organisations and individuals. We work collaboratively with them and have a Memorandum of Understanding with them to develop our partnership. We offer third sector specific savings products through our Anchor Savings Account and the more we take in deposits the more we can potentially lend to support third sector businesses.</p>
<p>SCRT’s vision is of an effective, socially-committed and prospering Scottish third sector sustained by a financial infrastructure consistent with it’s aims, values and needs.</p>
<h3>Why is ASB so interested in the third sector?</h3>
<p>We share many of the values of Scotland's third sector. We have our own social ethos and social mission. We are an independent mutual savings bank, the last in the UK. We have no paid trustees, no shareholders to satisfy and a fair wage culture. We don’t promote bonus driven lending and we invest depositors’ money in an ethical manner.</p>
<h3>What type of mortgages do you have?</h3>
<p>We have a full range of mortgages available from fixed to tracker and you can view them all on our website. When our advisor speaks with you we can help you to decide the most appropriate one for your circumstances, if you haven’t already decided.</p>
<h3>What is a fixed rate mortgage?</h3>
<p>A fixed rate mortgage is simply a means of guaranteeing your mortgage payment over a set period.</p>
<p>Fixed rates are for an initial period, we offer two-year, three-year and five-year fixed rate mortgages. After the fixed rate period ends, your mortgage will revert to a variable rate – normally our Standard Variable Rate payments.</p>
<h3>What is a tracker mortgage?</h3>
<p>Tracker mortgages are a type of variable rate mortgage. What makes them different from other variable rate mortgages is that they follow – track – movements of another rate most commonly the rate that is tracked is the Bank of England Base Rate. We offer a two-year tracker at a rate fixed above the Bank of England base rate</p>
<h3>What is a SVR mortgage?</h3>
<p>A Standard Variable Rate (SVR) is a type of mortgage where the interest rate is not fixed for a defined period of time. Pleased see our website for all of our current rates and products.  </p>
<h3>Do you do interest only mortgages?</h3>
<p>No.</p>
<h3>I have a mortgage with you and I think I might qualify for the social mortgage, is it retrospective?</h3>
<p>Sorry, no, this offer is for all new residential mortgage business from 27 May 2016.</p>
<h3>Do I have to attend a branch at any point?</h3>
<p>Most of the application can be completed after a phone call and by email. Currently, our procedures state you must be seen in a branch for identification and Anti-money laundering (AML) reasons. However, this will only be necessary after your mortgage is approved, so it’s not a wasted journey, and we are working on altering this procedure for those applicants who are not close to our location. Keep checking back here and we will update this when we find a suitable solution for people furthest away from our locations.</p>
<h3>How much can I borrow?</h3>
<p>It firstly depends on your circumstances. We need to ensure that you can meet the repayments now, and if something changes in the future. We have to lend responsibly. Please view our website for full details of how much you could borrow using our mortgage calculators.  </p>
<h3>Do you do 100% mortgages?</h3>
<p>No.</p>
<h3>How much do I need to put down as a deposit?</h3>
<p>We will consider mortgage applications up to 90% of the value / purchase price of the property whichever is lowest</p>
<h3>Are there any fees?</h3>
<p>At ASB we have a number of fixed rate mortgages that carry no arrangement fees or valuation fees - this will leave only your legal fees and a small transfer fee of £25. Full details will be given by your advisor.</p>
<h3>Will I have a named advisor</h3>
<p>Yes.</p>
<h3>Can I remortgage from my existing lender and still qualify?</h3>
<p>Yes, this is open to new customers and to existing customers with another lender</p>
<h3>What documentation will you need to see?</h3>
<p>We will explain all the proof of income and identification requirements when we speak with you but we will of course need to verify your income and your identity with original documentation.</p>
<h3>Do I have to be on a permanent contract?</h3>
<p>No. We will consider applications from people on fixed or renewable contracts, but as you might expect, there are some conditions, so come and talk to us and we can advise you.</p>
<h3>What’s the process for applying?</h3>
<p>If you apply online we can give you a decision in principle (DIP) within two hours.</p>
<p>Within 24 hours of you expressing an interest in the mortgage one of our colleagues will contact you.</p>
<p>Then, at a convenient time we have the main call which is when we advise whether we can lend to you, and what product is best suited to your needs. We will need about an hour on this call with you.</p>
<p>Finally, we process the paperwork which you will have sent to us by email or post. We need original documents but we will tell you all the requirements in or call with you.</p>
<h3>How long does it take?</h3>
<p>It is always good to have as much notice as possible, so we advise you to give us at least four weeks’ notice before you need the funds</p>
<h3>What insurances do I need?</h3>
<p>You will need to have Buildings Insurance covering the full reinstatement value of the property. Also, we will require you to have term or life assurance that covers the whole mortgage over the term.</p>
<h3>Are there any types of property that you won’t lend against?</h3>
<p>We will not lend on self-build, shared ownership or shared equity. We also won’t lend if the property is in a building with more than four storeys. Non-traditional build can also have some restrictions so best to talk to us about the accommodation first.</p>
<h3>How long is this Social:Mortgage offer available for?</h3>
<p>Currently it’s an indefinite offer, there is no end point.</p>
<h3>Can I nominate another third sector organisation other than the one I'm connected to?</h3>
<p>We will pay the cheque to the organisation that you are connected with, and who in turn has a connection to SCRT. Once the funds are with the third sector organisation, they are unrestricted.</p>
<h3>Is there anything else I should know?</h3>
<p>You must come directly to us – this offer is not available through a broker or intermediary.</p>
