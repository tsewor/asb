            <div class="row">
                <div class="cf margin-btm-large">
                    <div class="medium-12 large-12">
                        <p>You should read these conditions and our General Terms and Conditions carefully and contact us if there is anything you do not understand. Please keep all terms and conditions in a safe place for future reference.</p>
<p>The Cash ISA Account is a deposit account and is statement based.</p>
<p>1. Definitions</p>
<p>Some words and phrases have a specific meaning in these conditions. These are shown below:<br />- “Costs and Charges” means the money we are entitled to keep in accordance with condition 5. <br />- “ISA Manager” means a firm which is authorised to manage ISAs. <br />- “Tax Year” means the period from 6 April one year to 5 April of the following year. <br />- “ISA” means the Individual Savings Account.<br />- “ISA Regulations” means the Individual Savings Account Regulations 1998 (including changes made in the future).<br />- “HMRC” means HM Revenue &amp; Customs.<br /> <br />2. Eligibility</p>
<p>- A Cash ISA Account can only be opened in your name. Joint accounts are not permitted.<br />- You must be 16 years or over to open a Cash ISA Account<br />- You must be resident in the United Kingdom for tax purposes or, if not so resident, either perform duties which, by virtue of Section 28 of Income Tax (Earnings &amp; Pensions) Act 2003 (Crown employees serving overseas), are treated as being performed in the United Kingdom, or be married to, or in a civil partnership with, a person who performs such duties..<br />- You cannot open a Cash ISA Account with us if you have already opened and subscribed to another Cash ISA Account with another provider during the same Tax Year.<br />- You may subscribe to one Cash ISA and one Stocks &amp; Shares ISA in the same Tax year.<br />- If you are not sure if you are eligible to open a Cash ISA Account, you should contact HMRC.<br />- You will not be able to subscribe to an ISA if you cease to meet the UK residence qualification and must notify us of this. If we receive information suggesting an investor does not meet the residence qualification, we will request written confirmation as proof that he/she does meet the residence qualification.</p>
<p>3. Opening and paying into your Cash ISA Account</p>
<p>- To open an account you must complete an application for our Cash ISA Account and agree to deposit a minimum of £1. Your Cash ISA Account will be opened as soon as we accept your application and for new customers, where appropriate identification has been provided. If we do not accept your application, we will return any payment to you.<br />- Internet Banking facilities are available on your Cash ISA Account. You can apply for Internet Banking by completing an Internet Banking Application and returning it to the Bank.<br />- All deposits made into your Cash ISA Account must be made from your own money. You can make deposits over the counter in a branch by cash or cheque. You can also make deposits by internal transfer, or automated credit (such as CHAPS, BACS, and Faster Payments and Standing Order) provided you stay within your annual subscription limit, see 3.4. If you wish to make a deposit in person at one of our branches, you should bring your statement with you. <br />- You cannot transfer your rights to the cash to any other person and we will not recognise the interest or claim of any other person to the cash in your Cash ISA Account unless we are required to do so by law.<br />- You must not pay in more than the limits set down in the ISA Regulations in any one Tax Year. If you do, we may return the excess funds to any account you hold with us (on your own or with another person) or we may reject the payment in full and return it to you.<br />- If you do not pay into your Cash ISA Account during a Tax Year but want to pay in a following Tax Year, you are required under the ISA Regulations to make a new application before we can accept further payments.<br /> <br />4. Withdrawing money from your Cash ISA Account or closing it</p>
<p>- There is a maximum amount of cash that you can withdraw from your account in any one day. This amount may change from time to time and you can find out what the daily limit is by visiting or telephoning the Bank.<br />- You must give us 7 days notice to withdraw money from your Cash ISA Account by written instructions to your branch or completing a request to your branch on our Internet Banking page. After the 7 day period, we will automatically transfer the funds as per your instructions, to another Airdrie Savings Bank Account. If you give us notice to withdraw cash or to transfer the funds to another financial institution, but do not make the withdrawal within 30 days of your date of notice, you must give us the notice again.<br />- If you make a withdrawal from your Cash ISA Account in the Tax Year that you opened it, you will not be able to make further deposits if you have already paid in the maximum amount allowed by the ISA Regulations in a Tax Year.<br />- If you close your Cash ISA Account in the Tax Year that you opened it, you will not be able to open and subscribe to another Cash ISA Account in the same tax year with a different provider.<br />- The amount you may be able to withdraw or the amount you may receive when closing your Cash ISA Account, may be affected by any costs and charges that we keep under condition 7.<br />- Chequebooks, cards and overdraft facilities are not available on your Cash ISA Account.<br />- Direct Debits and Standing Orders are not available on your Cash ISA Account.<br />- Withdrawals cannot be made using our Internet Banking Service.<br />- You must not use your Cash ISA Account as security for any borrowing.<br />- There is no limitation on the frequency which you may issue withdrawal instructions.</p>
<p>5. Transfers</p>
<p>- You can transfer all or part of your Cash ISA Account to another ISA Manager if you complete a Transfer Instruction Form. We will process your request within five business days of your receipt but you can request a later date if you wish. We will send the balance of your Cash ISA Account, less any costs or charges, together with accompanying information to your new ISA Manager within five business days of receiving the request from your new ISA Manager, or on a date that we agree with the new ISA Manager. We will make any transfer in accordance with the ISA Regulations.<br />- We do not accept transfer requests received in from other banks or financial institutions.<br /> <br />6. When we may close your Cash ISA Account</p>
<p>- We will give you three months’ written notice if we plan to stop managing your Cash ISA Account. We may do this where we reasonably consider it is no longer practical for us to keep to the ISA Regulations or where we reasonably consider that managing your Cash ISA Account is no longer commercially viable for us. During the notice period, you can transfer your Cash ISA Account to another ISA Manager in accordance with condition 5.<br />- We may close your cash ISA Account if you withdraw all of the funds from it.<br />- We may close your Cash ISA Account if we discover (or HMRC tells us) that it has or will become “void”. We will give you written notice of this immediately and we will be required to close your Cash ISA Account without delay.<br />- If we reasonably believe there are circumstances that require us to close your Cash ISA Account immediately, we may do so without giving you prior notice. We will not be responsible to you for any losses that you suffer as a result.<br />- If we close your Cash ISA Account, we will follow the ISA Regulations and we will pay the proceeds of your Cash ISA Account, less any amount we are entitled to under condition 7.<br /> <br />7. Costs and Charges</p>
<p>- We are entitled to use any cash in your Cash ISA Account to pay our costs and charges and to pay any other necessary costs (such as tax) to HMRC or another government agency in relation to your Cash ISA Account.<br />- If we do not know how much the costs or charges will be, we may keep an amount of cash that is reasonable and appropriate.</p>
<p>8. Interest</p>
<p>- We will pay you gross interest on the daily cleared balance of your account, annually at the end of October each year. The interest rate is variable. This Cash ISA Account conforms to the Government Stakeholder Cash Deposits Account Standards and is guaranteed to offer an interest rate of no less that 1% below the Bank of England Base Rate<br />- We will pay interest to you at the Cash ISA Account rate shown in our Interest Rates and Charges leaflet.</p>
<p>9. Tax</p>
<p>- Under the ISA Regulations, you do not have to pay tax on the income or capital gains from your Cash ISAs, although this may change in the future.<br />- If you die, the exemption from tax under the ISA Regulations will cease and your Cash ISA Account will form part of your estate and may be subject to Inheritance Tax.<br />- Once we have received satisfactory proof of authority from your personal representatives, we will pay the proceeds of your Cash ISA Account to them, together with any interest earned but less any costs and charges.<br /> <br />10. Cancellation</p>
<p>- You can cancel your Cash ISA Account by writing to us within 14 days of making your first payment. We will return your money in full, together with any interest earned.</p>
<p>If you cancel in relation to the transfer of previous years’ contributions to an ISA, the associated tax advantages with those contributions will be lost in the future.</p>
