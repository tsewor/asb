                <div class="cf margin-btm-large">
                    <div class="medium-12 large-12">
                        <p>These notes provide information about the Anchor Savings Account and how it relates to the work of the Scottish Community Re-Investment Trust (SCRT). They do not provide financial advice</p>
<h3>What is SCRT?</h3>
<p>SCRT, the Scottish Community Re-Investment Trust is a Scottish Charitable Incorporated Organisation registered with the Office of the Scottish Charity Regulator. SCRT is the apex of a movement to develop the financial capability of the third sector in Scotland within the framework of a just financial system of shared values. This will allow the third sector, its supporters and stakeholders to invest in its own renewal and development, rather than depend on others or a trend towards commercialisation to support it.</p>
<p>It is a member led charity whose founding members include Penumbra, CEIS, Ekopia, Senscot, and two independent trustees, one of whom is chair of the board.</p>
<p>The first leg of SCRT’s work is a relationship with Airdrie Savings Bank to offer banking services to the third sector and its stakeholders.</p>
<h3>How does the Anchor savings account work and who can apply?</h3>
<p>The Anchor Savings Account is a term deposit account open initially to independent third sector organisations, their co-workers, beneficiaries, customers, and members.  Three savings’ periods are available: 30 days’ notice; 90 days’ notice; and 1-year fixed term.</p>
<p>Airdrie Savings Bank does not rely on the wholesale money markets to finance its lending activities. It lends customers’ money largely within the small business community and for responsible property development in Scotland. As a community based bank it now wishes to build its relationships with the third sector through general banking services as well as lending. Growing levels of deposits in the Anchor Savings Account will support this.</p>
<h3>I can earn more elsewhere. Why is the return not a market leader?</h3>
<p>The interest rates payable on deposits in the Anchor Savings Account are on a par with the bank’s other retail savings accounts. Its business model is different to the large mainstream banks. Airdrie Savings Bank operates in the real economy and does not seek to make profit from financial speculation. Its unlent balances have to be placed with a small list of counterparties including the Bank of England, approved by the regulators and on which it earns currently next to nothing. As a community based bank with an overriding emphasis on customer service, it tries to pay what it considers to be a just and sustainable financial return rather than a market leading return.</p>
<p>Airdrie Savings Bank provides a socially responsible home for your savings where you can know they are working in line with your values.</p>
<h3>How come I haven't heard of it before?</h3>
<p>Although Airdrie Savings Bank is 180 years old, it has only more recently undertaken a prudent programme of expansion and profile raising. The Anchor Savings Account is very new, launched only at the end of 2014.</p>
<h3>Can I choose what my money goes towards?</h3>
<p>Airdrie Savings Bank does not offer targeted accounts. As a bank, it must decide where it lends its money based upon detailed due diligence and risk appetite. However, as deposits grow within the Anchor Savings Account, the bank will have more capacity to provide affordable low risk loans to the third sector.</p>
<h3>How will depositors know what their money is doing?</h3>
<p>Both Airdrie Savings Bank and SCRT will post information on their respective websites. SCRT will also provide a newsletter updating everyone interested.</p>
<h3>But what happens if the bank's loans default? Is my money at risk?</h3>
<p>There can be no guarantee that all borrowers will pay back their loans within the agreed terms. Airdrie Savings Bank is in the same position as any other lending institution in that respect. However, the bank has a successful 180 year track record. As a matter of prudential management, it maintains a loan loss reserve sufficient to cover the anticipated risk of loss in the portfolio. All lending is assessed rigorously and money is not lent to applicants that the bank does not think are sustainable or where the loan might put the borrower or its beneficiaries at risk. The bank has a relatively low risk profile and as an authorised bank is required to keep its lending within prudential limits.</p>
<p>Airdrie Savings Bank is a member of the Financial Services Compensation Scheme. The Scheme covers individuals, unincorporated trusts and organisations, as well as smaller incorporated third sector organisations.</p>
<p>On 3 July 2015, the Prudential Regulation Authority announced changes to the FSCS compensation limit. For those depositors covered bythe FSCS prior to 3 July, (individuals and small companies) their limit of £85,000 will be maintained until 31 December 2015. After that it will change to £75,000.  For those depositors who became eligible for cover by the FSCS from 3 July onwards (such as large corporates), their compensation limit is £75,000 from 3 July 2015.</p>
<p>Any deposits you hold above the limit are unlikely to be covered.</p>
<p>For more detailed information on eligibility refer to the FSCS website, <a href="http://www.fscs.org.uk/">http://www.fscs.org.uk</a> or call the bank on .</p>
<h3>Can I donate my interest?</h3>
<p>Yes, you can donate your interest to charity. As SCRT is a registered Scottish charity you can donate your interest to SCRT. If you are a taxpayer you may be eligible for tax relief and able to gift aid your donation.</p>
