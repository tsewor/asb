                <h1 class="tera">Cash ISA terms and conditions</h1>
                <p class="alpha">These are the specific terms and conditions for our Cash ISA Account. These conditions apply in addition to our General Terms and Conditions. If there is any conflict between these conditions and our General Terms and Conditions, the provisions of these conditions will apply to the extent of the conflict. The words and phrases used in these conditions have the same meanings as those shown in the Definitions section of the General Terms and Conditions.</p>
