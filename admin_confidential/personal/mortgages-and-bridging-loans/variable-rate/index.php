                    <h2 class="alpha">Two-year Tracker mortgage</h2>
<p>With our Tracker mortgage, the interest rate you pay on your mortgage is set at 1.25% above the Bank of England base rate.</p>
<ul class="tick-list">
<li>The initial rate is 1.25% above the Bank of England base rate until April 2018, to give a current rate payable of 1.75%. Reverting to the Airdrie Savings Bank standard variable rate thereafter, currently 4.50%. Overall cost for comparison 4.2% APRC.</li>
<li>Mortgage interest rate is adjusted in line with the Bank of England base rate.</li>
<li>Maximum loan to value 75%.</li>
<li>Portable</li>
<li>£1,090 arrangement fee.</li>
<li>Standard valuation fee paid by Airdrie Savings Bank (max £450).</li>
<li>Early repayment charge of 2% applies during the initial tracker period.</li>
<li>Overpayments permitted at any time without penalty.</li>
</ul>
<p> </p>
<h2 class="alpha">Standard Variable rate mortgage</h2>
<p>Our most flexible mortgage, with repayments to suit your circumstances and the ability to overpay at no cost, whenever it suits you.</p>
<ul class="tick-list">
<li>Airdrie Savings Bank standard variable rate, currently 4.50%. Overall cost for comparison 4.6% APRC.</li>
<li>Maximum loan to value 80%.</li>
<li>Portable</li>
<li>£495 arrangement fee.</li>
<li>No early repayment charges.</li>
<li>Overpayments permitted at any time without penalty.</li>
</ul>
