                        <h2>Savings Account Terms and Conditions</h2>
<p>These are the specific terms and conditions for our Savings Account. These conditions apply in addition to our General Terms and Conditions, available from any branch or by calling the Customer Contact Centre on 01236-766851. If there is any conflict between these conditions and our General Terms and Conditions, the provisions of these conditions will apply to the extent of the conflict. The words and phrases used in these conditions have the same meanings as those shown in the Definitions section of the General Terms and Conditions. </p>
<p>You should read these conditions and our General Terms and Conditions carefully and contact us if there is anything you do not understand. Please keep all terms and conditions in a safe place for future reference. </p>
<p>The Savings Account is a deposit account and is passbook based.</p>
<h3>1 Eligibility</h3>
<p>1.1 A Savings Account can be opened in sole or joint names. </p>
<p>1.2 To open an account you must complete an application for our Savings Account and agree to deposit a minimum of £1. Your Savings Account will be opened as soon as we accept your application and issue you with a passbook. </p>
<p>1.3 There is no maximum limit to the amount you can hold in your Savings Account at any time. </p>
<p>1.4 Internet Banking facilities are available on our Savings Account. Internet Banking is available to customers aged 16 years or older. You can apply for Internet Banking by completing an Internet Banking Application and delivering it to your branch.</p>
<h3>2 Paying money in</h3>
<p>2.1 You can make deposits into your Savings Account over the counter in a branch, by cash or cheque, by presenting your passbook. Your Savings Account also accepts internal transfers and automated credits (such as CHAPS, BACS or Faster Payments).</p>
<h3>3 Withdrawing money</h3>
<p>3.1 There is a maximum amount of cash that you can withdraw from your account in any one day. This amount may change from time to time and you can find out what the daily limit is by visiting or telephoning your branch. </p>
<p>3.2 You can withdraw money from your Savings Account by presenting your passbook at your branch. </p>
<p>3.3 If you are registered for Internet Banking you will also be able to make transfers to and from your Savings Account to your other Airdrie Savings Bank accounts or other bank accounts held with other financial institutions. Our charges for these services are set out in our Interest Rates and Charges leaflet. We may vary our charges in accordance with our General Terms and Conditions. </p>
<p>3.4 Chequebooks, cards, and overdraft facilities are not available on your Savings Account. </p>
<p>3.5 Direct Debits and Standing Orders are not available on your Savings Account.</p>
<h3>4 Interest</h3>
<p>4.1 Interest on money we owe you is calculated on the daily cleared balance of your account and paid twice yearly to your Savings Account at the end of April and October. The interest rate is variable.</p>
<p>4.2 The rate of interest we pay on money we owe you in your Savings Account is tiered. This means that we may pay a different rate of interest according to whether the account balance exceeds a particular level. Further details of the tiers are set out in our Interest Rates and Charges leaflet.</p>
<p> </p>
<h2><a id="premium"></a>Premium Account Terms and Conditions</h2>
<p>These are the specific terms and conditions for our Premium Account.  These conditions apply in addition to our General Terms and Conditions.  If there is any conflict between these conditions and our General Terms and Conditions, the provisions of these conditions will apply to the extent of the conflict.  The words and phrases used in these conditions have the same meanings as those shown in the Definitions section of the General Terms and Conditions.</p>
<p>You should read these conditions and our General Terms and Conditions carefully and contact us if there is anything you do not understand.  Please keep all terms and conditions in a safe place for future reference.</p>
<p>The Premium Account is a deposit account and is passbook-based.</p>
<h3>1 Eligibility</h3>
<p>1.1 A Premium Account can be opened in sole or joint names.</p>
<p>1.2 To open an account you must complete an application for our Premium Account and agree to deposit a minimum of £1,000.  Your Premium Account will be opened as soon as we accept your application and issue you with a passbook.</p>
<p>1.3 You must keep the balance of this account above £1,000 at all times.  In the event that it falls below £1,000 we will automatically close your Premium Account and transfer your balance with interest to an Airdrie Savings Bank Savings account which will be opened on your behalf if you do not already hold one.</p>
<p>1.4 There is no maximum limit to the amount you can hold in your Premium Account at any time.  </p>
<p>1.5 Internet Banking facilities are available on our Premium Account. Internet Banking is available to customers aged 16 years or over. You can apply for Internet Banking by completing an Internet Banking Application and delivering it to your branch.</p>
<h3>2 Paying money in</h3>
<p>2.1 You can make deposits into your Premium Account over the counter in a branch by cash, or cheque, or by internal transfer or automated credit (e.g.  CHAPS, BACS or Faster Payments). If you wish to make a deposit in person at one of our branches you should bring your passbook with you.</p>
<h3>3 Withdrawing money</h3>
<p>3.1 There is a maximum amount of cash that you can withdraw from your account in any one day. This amount may change from time to time and you can find out what the daily limit is by visiting or telephoning your branch.</p>
<p>3.2 You can withdraw money from your Premium Account by presenting your Premium Account passbook at your branch.</p>
<p>3.3 Chequebooks, cards, and overdraft facilities are not available on your Premium Account and automated payments such as standing orders and Direct Debits cannot be made from this account.</p>
<p>3.4 Withdrawals cannot be made using our Internet Banking Service.</p>
<h3>4 Interest</h3>
<p>4.1 We will pay interest to you at the Premium Account rate shown in our Interest Rate and Charges leaflet, provided you give us 90 days' notice of any withdrawals. The interest rate is variable.</p>
<p>4.2 If you do not give us 90 days’ notice of a withdrawal, you will lose 90 day’s interest that would have been due on the amount of the withdrawal.  You can, however, withdraw the last two interest payments we have made to your account, without notice and without losing any interest on the amount of the withdrawal.</p>
<p>4.3 Interest on money we owe you is calculated on the daily cleared balance of your account and paid twice yearly to your Premium Account in April and October of each year. </p>
<p>4.4 The rate of interest we pay on money we owe you in your Premium Account is tiered. This means that we may pay a different rate of interest according to whether the account balance exceeds a particular level.  Further details of the tiers are set out in our Interest Rate and Charges leaflet.</p>
